    public void matched(Client client, Location loc) {
	client.setLookingNow(false);
    client.setOccupiedLoc(loc.getAddress());
}

    public void match(Client client, Location loc) {
	if (client.getFacilityPref() == loc.getFacility()) {
    	matched(client, loc);
        } 
    }
    
    // FILE IO methods
    private void readFromClientsFile() {
        Readfile rf = new Readfile("clients.txt");
        String input = rf.readRecord();
        while (input != null) {
            String[] firstSplit = input.split(";");
            
            Client newClient = new Client();
            newClient.deserializeClient(firstSplit[0]);
            
            Facility newFacility = new Facility();
            newFacility.deserialize(firstSplit[1]);
            
            newClient.setFacilityPref(newFacility);
            clientArrayList.add(newClient);
            input = rf.readRecord();
            
        }
        rf.close();
    }
    private void writeToClientsFile() {
        Writefile wf = new Writefile("clients.txt");
        for (Client client : clientArrayList) {
            String output = client.toString();
            wf.write(output);
        }
        wf.close();
    }
