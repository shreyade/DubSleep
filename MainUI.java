    private void updateClientList() {
        String[] displayClients = new String[clientArrayList.size()];
        for (int i = 0; i < clientArrayList.size(); i++) {
            displayClients[i] = clientArrayList.get(i).getName();
        }
        
        //assign the titlesarray to the list
        clientList.setListData(displayClients);
    }
    
